package day16_loops;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("------------------------------------");

        int num = 0;

        while (num <= 5){
            System.out.println(num + ") Hello World");
            num++;
        }
    }
}
