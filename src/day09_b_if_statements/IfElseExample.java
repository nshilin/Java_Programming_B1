package day09_b_if_statements;

public class IfElseExample {

    public static void main(String[] args) {

        int result = 50;
        int passRate = 65;

        //65 and higher is passing
        if(result>=passRate){
            System.out.println("The exam is passed");
        }else{
            System.out.println("The exam is not passed");
        }
        }

}

