package day07_relational_operators;

public class UpdatedVariables {
    public static void main(String[] args) {

        int a = 10;
        a = 11;

        a++; // reassign a = a + 1; --- >
        //System.out.println(a++); //11;

        System.out.println(a);     //12

        ++a;    //reassign a = a + 1;
        System.out.println(a);     //13

        int x = 10;
        x = x + 4;

        x += 4;
        System.out.println(x);      //18




    }
}
